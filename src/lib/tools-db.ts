export function getToolById(id: string): Tool | undefined {
  return TOOLS.find((t) => t.id === id);
}

export function getToolsByIds(ids: string[]): Tool[] {
  return ids
    .map((id) => getToolById(id))
    .filter((t): t is Tool => t !== undefined);
}

export function getToolsByCategory(category: ToolCategory): Tool[] {
  return TOOLS.filter((t) => t.category === category);
}

export function getNoCodeTools(): Tool[] {
  return TOOLS.filter((t) => t.userEffort !== 'write-code');
}

export function getToolsByCapability(capability: Capability): Tool[] {
  return TOOLS.filter((t) => t.capabilities.includes(capability));
}

export function findToolByName(name: string): Tool | undefined {
  const normalized = name.toLowerCase().trim();
  return TOOLS.find(
    (t) =>
      t.name.toLowerCase() === normalized ||
      t.id.toLowerCase() === normalized ||
      (t.aliases && t.aliases.some((a) => a.toLowerCase() === normalized))
  );
}

export function toolsOverlap(tool1: Tool, tool2: Tool): Capability[] {
  return tool1.capabilities.filter((c) => tool2.capabilities.includes(c));
}
